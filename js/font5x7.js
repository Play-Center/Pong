export function drawGlyph(ch, x, y, cell, g2d, color = WHITE) {
  const grid = FONT5x7[ch];
  if (!grid) return;
  g2d.fillStyle = color;
  for (let r = 0; r < 7; r++) {
    for (let cCol = 0; cCol < 5; cCol++) {
      if (grid[r][cCol]) {
        g2d.fillRect(snap(x + cCol * cell), snap(y + r * cell), cell, cell);
      }
    }
  }
}

export function measureTextBlocks(str, cell, gap = cell) {
  const n = str.length;
  return { width: n * (5 * cell) + (n - 1) * gap, height: 7 * cell };
}

export function drawTextBlocks(str, centerX, topY, cell, g2d, color = WHITE, gap = cell) {
  const { width } = measureTextBlocks(str, cell, gap);
  let x = centerX - width / 2;
  for (const ch of str.toUpperCase()) {
    drawGlyph(ch, x, topY, cell, g2d, color);
    x += 5 * cell + gap;
  }
}


